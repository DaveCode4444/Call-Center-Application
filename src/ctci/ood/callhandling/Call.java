package ctci.ood.callhandling;

/**
 * This is the Call class that acts as an incoming call
 * 
 * @author Devang Sawant
 *
 */

public class Call {
	
	//each call will have a unique id
	private int id;

	public Call(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
	
	

}
